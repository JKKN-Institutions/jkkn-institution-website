'use client'

import { cn } from '@/lib/utils'
import type { TestimonialsProps } from '@/lib/cms/registry-types'

export default function Testimonials({
  testimonials = [],
  layout = 'carousel',
  autoplay = true,
  showRating = true,
  className,
  isEditing,
}: TestimonialsProps) {
  if (testimonials.length === 0 && isEditing) {
    return (
      <section className={cn('py-16 px-4', className)}>
        <div className="container mx-auto">
          <div className="p-8 border-2 border-dashed border-muted-foreground/25 rounded-lg">
            <p className="text-muted-foreground text-center">Click to add testimonials</p>
          </div>
        </div>
      </section>
    )
  }

  if (testimonials.length === 0) return null

  return (
    <section className={cn('py-16 px-4', className)}>
      <div className="container mx-auto">
        <div
          className={cn(
            layout === 'grid' && 'grid gap-8 md:grid-cols-2 lg:grid-cols-3',
            layout === 'carousel' && 'flex gap-6 overflow-x-auto snap-x snap-mandatory pb-4'
          )}
        >
          {testimonials.map((testimonial, index) => (
            <div
              key={index}
              className={cn(
                'bg-card rounded-lg p-6 shadow-sm',
                layout === 'carousel' && 'min-w-[300px] flex-shrink-0 snap-center'
              )}
            >
              {showRating && testimonial.rating !== undefined && (
                <div className="flex gap-1 mb-4">
                  {Array.from({ length: 5 }).map((_, i) => (
                    <svg
                      key={i}
                      className={cn(
                        'w-5 h-5',
                        i < testimonial.rating! ? 'text-yellow-400 fill-current' : 'text-muted'
                      )}
                      viewBox="0 0 20 20"
                    >
                      <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
                    </svg>
                  ))}
                </div>
              )}
              <blockquote className="text-muted-foreground mb-4">
                &ldquo;{testimonial.content}&rdquo;
              </blockquote>
              <div className="flex items-center gap-3">
                {testimonial.avatar && (
                  <img
                    src={testimonial.avatar}
                    alt={testimonial.name}
                    className="w-12 h-12 rounded-full object-cover"
                  />
                )}
                <div>
                  <p className="font-medium">{testimonial.name}</p>
                  {testimonial.role && (
                    <p className="text-sm text-muted-foreground">{testimonial.role}</p>
                  )}
                </div>
              </div>
            </div>
          ))}
        </div>
      </div>
    </section>
  )
}
